package execicio4;

public class Carga {
	
	private double peso;
	private double valor;
	
	public Carga() {

	}
	
	public Carga(double peso, double valor) {
		super();
		this.peso = peso;
		this.valor = valor;
	}
	
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	
	

}
